#!/usr/bin/env python3
"""Run a skill-efficacy benchmark, merge pass@k results, and flag failing instances.

Usage:
    python scripts/msbench-eval.py --benchmark cosmos-sdk-skills --repeat 3 --threshold 0.9
    python scripts/msbench-eval.py --dry-run

The script submits a single ``msbench-cli run`` command, optionally waits for the
run to finish by polling ``msbench-cli resume``, generates a merged report with
``msbench-cli report --merge pass_at_k``, parses per-instance pass rates from the
report JSON, and invokes ``scripts/create-skills-issue.py`` when any scenario × SDK
instance falls below the configured threshold.
"""

from __future__ import annotations

import argparse
import io
import json
import os
import re
import shutil
import subprocess
import sys
import time
import zipfile
from collections import defaultdict
from pathlib import Path
from typing import Any

DEFAULT_BENCHMARK = "cosmos-sdk-skills"
DEFAULT_REPEAT = 3
DEFAULT_THRESHOLD = 0.9
DEFAULT_OUTPUT = "results.json"

# Repository layout. The task images are Azure Linux (tdnf) based and are built
# by the official harbor-format-curation pipeline so the remote execution backend runs the baked
# /entry.sh verifier and produces a real eval.json score. The images pre-install
# zip/unzip/which (the remote backend's agent harness cannot install them on tdnf) and bake the
# @github/copilot CLI, so the agent is driven by shared/ces/runner.sh via
# --runner, authenticated with GITHUB_TOKEN via --encrypted-env. cosmos-sdk-skills
# is not centrally registered, so the harbor-generated dataset is supplied via
# --dataset (regenerated by `harbor-format-curation update-database`).
REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_RUNNER = REPO_ROOT / "benchmarks" / "cosmos-sdk-skills" / "shared" / "ces" / "runner.sh"
DEFAULT_DATASET = REPO_ROOT / "benchmarks" / "cosmos-sdk-skills" / "dataset.jsonl"
# Remote execution backend. Kept out of the public repo; supplied via the
# MSBENCH_BACKEND env var (set from a repo variable in CI, or exported locally).
# Empty by default so --backend is simply omitted unless configured.
DEFAULT_BACKEND = os.environ.get("MSBENCH_BACKEND", "")
# Env vars the runner needs in-container; forwarded with --encrypted-env when set.
RUNNER_TOKEN_ENV = "GITHUB_TOKEN"
COPILOT_MODEL_ENV = "COPILOT_MODEL"
# Live Cosmos account credentials. When any of these are present in the
# environment they are forwarded (encrypted) so the verifier grades against a
# real account instead of the bundled emulator. The verifier (shared/verifier/
# runner.sh) accepts EITHER a single COSMOS connection string OR a split
# COSMOS_ENDPOINT/COSMOS_KEY pair; if none are set the run uses the emulator.
LIVE_ACCOUNT_ENVS = ("COSMOS", "COSMOS_ENDPOINT", "COSMOS_KEY")
# The evaluation CLI requires --agent alongside --runner. The runner overrides the launch
# script so this plugin's agent-downloader (which fails on the Mariner image) is
# never invoked; the named agent only supplies the host orchestration + model map.
DEFAULT_RUNNER_AGENT = "github-copilot-cli"
# The github-copilot-cli special agent requires --model even when a --runner
# overrides the launch. This must be a key in the plugin's models map.
RUNNER_PLUGIN_MODELS = {
    "default",
    "claude-sonnet-4-20250514",
    "gpt-4.1",
    "claude-sonnet-4.6",
}
# Use the key that also matches runner.sh's own COPILOT_MODEL default.
DEFAULT_RUNNER_MODEL = "claude-sonnet-4.6"
POLL_INTERVAL_SECONDS = 30
MAX_POLLS = 120
# Web UI where a submitted run can be viewed/analyzed. The run_id is the only
# variable; the host is stable across backends. Surfaced in stdout and the
# results.json artifact so a human (or the failure issue) can open the run.
RUN_ANALYSIS_URL_TEMPLATE = "https://msbenchapp.azurewebsites.net/run-analysis/{run_id}"
RUN_ID_PATTERNS = (
    r'"run_id"\s*:\s*"([^"]+)"',
    r"\brun[_ -]?id\b\s*[:=]\s*([A-Za-z0-9._:-]+)",
    r"\bRun ID\b\s*[:=]\s*([A-Za-z0-9._:-]+)",
)
STATUS_PATTERNS = (
    r'"status"\s*:\s*"([^"]+)"',
    r"\bstatus\b\s*[:=]\s*([A-Za-z0-9._-]+)",
)
TERMINAL_SUCCESS = {"completed", "complete", "succeeded", "success", "finished", "done"}
TERMINAL_FAILURE = {"failed", "error", "cancelled", "canceled", "aborted"}
NON_TERMINAL = {"queued", "pending", "running", "resuming", "in_progress", "in-progress", "submitted"}
PASS_RATE_KEYS = (
    "pass_at_k",
    "pass@k",
    "pass_rate",
    "passrate",
    "pass",
    "score",
)
INSTANCE_ID_KEYS = ("instance_id", "instance", "instance_name", "id", "name", "task_id")
SCENARIO_KEYS = ("scenario", "scenario_name")
SDK_KEYS = ("sdk", "sdk_name", "language", "lang")
SKIP_CONTAINER_KEYS = {"summary", "overall", "aggregated", "aggregate", "totals", "metadata"}


class MsbenchEvalError(RuntimeError):
    """Raised when the benchmark workflow cannot complete."""


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run a skill-efficacy benchmark and evaluate per-instance pass thresholds."
    )
    parser.add_argument("--benchmark", default=DEFAULT_BENCHMARK, help="Benchmark name")
    parser.add_argument("--repeat", type=int, default=DEFAULT_REPEAT, help="Repeat count")
    parser.add_argument(
        "--threshold",
        type=float,
        default=DEFAULT_THRESHOLD,
        help="Minimum acceptable pass rate from 0.0 to 1.0",
    )
    parser.add_argument("--output", default=DEFAULT_OUTPUT, help="Report output JSON path")
    parser.add_argument("--agent", help="Optional msbench agent value")
    parser.add_argument(
        "--model",
        help=(
            "Optional Copilot CLI model. Valid msbench plugin model keys are also "
            "passed to msbench; other values are forwarded only as COPILOT_MODEL."
        ),
    )
    parser.add_argument(
        "--runner",
        default=str(DEFAULT_RUNNER),
        help=(
            "Path to the runner script that drives the baked @github/copilot CLI. "
            "Defaults to the repo's shared/ces/runner.sh. Pass an empty string to omit."
        ),
    )
    parser.add_argument(
        "--dataset",
        default=str(DEFAULT_DATASET),
        help=(
            "Benchmark dataset path. cosmos-sdk-skills is not centrally registered, "
            "so the dataset must be supplied. Pass an empty string to omit."
        ),
    )
    parser.add_argument(
        "--backend",
        default=DEFAULT_BACKEND,
        help="Remote execution backend (required for --encrypted-env).",
    )
    parser.add_argument(
        "--encrypted-env",
        dest="encrypted_env",
        action="append",
        default=None,
        help=(
            "Env var name(s) to forward into the container encrypted (remote backend only). "
            "Defaults to forwarding GITHUB_TOKEN when it is set in the environment."
        ),
    )
    parser.add_argument(
        "--no-wait",
        action="store_true",
        help="Submit the run and exit without polling/report generation",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print commands without executing them",
    )
    args = parser.parse_args()

    if args.repeat <= 0:
        parser.error("--repeat must be greater than zero")
    if not 0 <= args.threshold <= 1:
        parser.error("--threshold must be between 0 and 1")

    if args.runner and not Path(args.runner).is_file():
        parser.error(f"--runner script not found: {args.runner}")
    if args.dataset and not Path(args.dataset).is_file():
        parser.error(f"--dataset file not found: {args.dataset}")
    # The evaluation CLI requires --agent whenever --runner is used.
    if args.runner and not args.agent:
        args.agent = DEFAULT_RUNNER_AGENT
    args.copilot_model = None
    # The workflow's --model selects the baked Copilot CLI model via COPILOT_MODEL.
    # The evaluation CLI still requires --model to be a plugin model-map key, so unsupported
    # Copilot model names fall back to a safe plugin key for host orchestration.
    if args.runner and args.agent == DEFAULT_RUNNER_AGENT:
        if args.model:
            args.copilot_model = args.model
            os.environ[COPILOT_MODEL_ENV] = args.copilot_model
            if args.model not in RUNNER_PLUGIN_MODELS:
                args.model = DEFAULT_RUNNER_MODEL
        else:
            args.model = DEFAULT_RUNNER_MODEL
    encrypted_env = _resolve_encrypted_env(args)
    if args.runner and RUNNER_TOKEN_ENV not in encrypted_env:
        print(
            f"WARNING: runner {args.runner} needs {RUNNER_TOKEN_ENV} in-container, but "
            f"{RUNNER_TOKEN_ENV} is not set or forwarded. The agent will fail to "
            "authenticate. Set a Copilot-enabled GITHUB_TOKEN.",
            file=sys.stderr,
        )

    return args


def quote_command(command: list[str]) -> str:
    return subprocess.list2cmdline(command)


def ensure_msbench_installed(dry_run: bool) -> None:
    if dry_run:
        return
    if shutil.which("msbench-cli") is None:
        raise MsbenchEvalError(
            "msbench-cli was not found on PATH. Install it first (for example: pip install msbench-cli)."
        )


def run_command(
    command: list[str],
    *,
    dry_run: bool = False,
    cwd: Path | None = None,
    check: bool = True,
) -> subprocess.CompletedProcess[str] | None:
    rendered = quote_command(command)
    print(f"> {rendered}")
    if dry_run:
        return None

    try:
        completed = subprocess.run(
            command,
            cwd=str(cwd) if cwd else None,
            text=True,
            capture_output=True,
            check=False,
        )
    except FileNotFoundError as exc:
        raise MsbenchEvalError(f"Command not found: {command[0]}") from exc
    except OSError as exc:
        raise MsbenchEvalError(f"Failed to start command: {rendered}\n{exc}") from exc

    if completed.stdout:
        print(completed.stdout.rstrip())
    if completed.stderr:
        print(completed.stderr.rstrip(), file=sys.stderr)

    if check and completed.returncode != 0:
        error_hint = ""
        combined = f"{completed.stdout}\n{completed.stderr}".lower()
        if "login" in combined or "unauthorized" in combined or "forbidden" in combined or "auth" in combined:
            error_hint = "\nAuthentication may be required. Verify your msbench/Azure credentials."
        raise MsbenchEvalError(
            f"Command failed with exit code {completed.returncode}: {rendered}{error_hint}"
        )

    return completed


def extract_run_id(text: str) -> str | None:
    for pattern in RUN_ID_PATTERNS:
        match = re.search(pattern, text, flags=re.IGNORECASE)
        if match:
            return match.group(1).strip().strip('"')
    return None


def run_analysis_url(run_id: str) -> str:
    """Build the web analysis URL for a submitted run."""
    return RUN_ANALYSIS_URL_TEMPLATE.format(run_id=run_id)


def print_run_link(run_id: str) -> None:
    """Emit the run's analysis link prominently so it can be opened/clicked."""
    print(f"\nRun analysis: {run_analysis_url(run_id)}")


def extract_status(text: str) -> str | None:
    for pattern in STATUS_PATTERNS:
        match = re.search(pattern, text, flags=re.IGNORECASE)
        if match:
            return match.group(1).strip().strip('"').lower()
    lowered = text.lower()
    for status in TERMINAL_SUCCESS | TERMINAL_FAILURE | NON_TERMINAL:
        if re.search(rf"\b{re.escape(status)}\b", lowered):
            return status
    return None


def _resolve_encrypted_env(args: argparse.Namespace) -> list[str]:
    """Env var names to forward with --encrypted-env."""
    env_names = list(args.encrypted_env or [])
    if not env_names:
        # Default (workflow) path: forward the Copilot token plus any live Cosmos
        # account credentials present in the environment. When no live-account
        # vars are set the verifier falls back to the bundled emulator.
        if os.getenv(RUNNER_TOKEN_ENV):
            env_names.append(RUNNER_TOKEN_ENV)
        for name in LIVE_ACCOUNT_ENVS:
            if os.getenv(name):
                env_names.append(name)
    if getattr(args, "copilot_model", None):
        env_names.append(COPILOT_MODEL_ENV)
    return list(dict.fromkeys(env_names))


def _append_common_run_flags(command: list[str], args: argparse.Namespace) -> None:
    if args.agent:
        command.extend(["--agent", args.agent])
    if args.model:
        command.extend(["--model", args.model])
    if getattr(args, "runner", None):
        command.extend(["--runner", args.runner])
    if getattr(args, "dataset", None):
        command.extend(["--dataset", args.dataset])
    if getattr(args, "backend", None):
        command.extend(["--backend", args.backend])
    # msbench-cli's --encrypted-env is nargs='+': all names MUST share one flag.
    # Repeating the flag (--encrypted-env A --encrypted-env B) makes argparse keep
    # only the LAST value, silently dropping the rest.
    encrypted_env = _resolve_encrypted_env(args)
    if encrypted_env:
        command.append("--encrypted-env")
        command.extend(encrypted_env)


def build_run_command(args: argparse.Namespace) -> list[str]:
    command = [
        "msbench-cli",
        "run",
        "--benchmark",
        args.benchmark,
        "--repeat",
        str(args.repeat),
    ]
    _append_common_run_flags(command, args)
    if getattr(args, "no_wait", False):
        # Submit and return immediately with the server-assigned run_id instead of
        # blocking until the agent run finishes. Used by the remote-backend submit
        # preflight to prove the calling identity can authenticate to the remote backend and is entitled to
        # create a run, without paying for a full grade.
        command.append("--no-wait")
    return command


def build_resume_command(run_id: str, args: argparse.Namespace) -> list[str]:
    # resume only accepts run identity + backend; run-config flags (agent, model,
    # runner, dataset, encrypted-env) are rejected here.
    command = ["msbench-cli", "resume", "--run_id", run_id]
    if getattr(args, "backend", None):
        command.extend(["--backend", args.backend])
    return command


def build_report_command(run_id: str, output_path: Path) -> list[str]:
    return [
        "msbench-cli",
        "report",
        "--run_id",
        run_id,
        "--merge",
        "pass_at_k",
        "--output",
        str(output_path),
    ]


def resolve_output_path(raw_output: str) -> Path:
    output_path = Path(raw_output)
    if not output_path.is_absolute():
        output_path = Path.cwd() / output_path
    output_path.parent.mkdir(parents=True, exist_ok=True)
    return output_path


def wait_for_completion(run_id: str, args: argparse.Namespace) -> str:
    """Poll ``msbench-cli resume`` until the run finishes.

    Returns the combined stdout/stderr of the terminal resume call (which
    includes the ``Saved results to <path>`` lines for every batch member).
    """
    print(f"Polling run {run_id} until completion...")
    for attempt in range(1, MAX_POLLS + 1):
        completed = run_command(build_resume_command(run_id, args), dry_run=False, check=False)
        text = f"{completed.stdout}\n{completed.stderr}" if completed else ""
        status = extract_status(text)

        if status in TERMINAL_SUCCESS:
            print(f"Run {run_id} completed with status: {status}")
            return text
        if status in TERMINAL_FAILURE:
            # msbench-cli mis-parses harbor eval.json and can surface a bogus
            # "error" status once the run finishes. Defer the real verdict to
            # eval.json grading instead of aborting here; if the run genuinely
            # failed with no gradable results, the downstream report fallback
            # (check=True) still surfaces the error loudly.
            print(
                f"Run {run_id} reported terminal status '{status}'; "
                "proceeding to authoritative eval.json grading."
            )
            return text

        print(
            f"Run {run_id} status after poll {attempt}/{MAX_POLLS}: {status or 'unknown'}; "
            f"sleeping {POLL_INTERVAL_SECONDS}s"
        )
        time.sleep(POLL_INTERVAL_SECONDS)

    raise MsbenchEvalError(
        f"Run {run_id} did not reach a terminal state after {MAX_POLLS} polls."
    )


def normalize_rate(value: Any) -> float | None:
    """Normalize a pass rate to a 0.0–1.0 float.

    Handles: bool, numeric (0-1 as fraction, >1 as percentage), and strings
    with optional '%' suffix. A '%' suffix always means the number is a percentage.
    """
    if isinstance(value, bool):
        return 1.0 if value else 0.0
    if isinstance(value, (int, float)):
        rate = float(value)
        if rate > 1.0 and rate <= 100.0:
            return rate / 100.0
        if 0.0 <= rate <= 1.0:
            return rate
        return None
    if isinstance(value, str):
        is_percent = value.strip().endswith("%")
        stripped = value.strip().rstrip("%")
        try:
            parsed = float(stripped)
        except ValueError:
            return None
        if is_percent:
            return parsed / 100.0
        return normalize_rate(parsed)
    return None


def first_present(mapping: dict[str, Any], keys: tuple[str, ...]) -> Any:
    for key in keys:
        if key in mapping and mapping[key] not in (None, ""):
            return mapping[key]
    return None


def extract_instance_results(payload: Any) -> list[dict[str, Any]]:
    extracted: dict[str, dict[str, Any]] = {}

    def visit(node: Any, context: dict[str, Any]) -> None:
        if isinstance(node, list):
            for item in node:
                visit(item, context)
            return

        if not isinstance(node, dict):
            return

        next_context = dict(context)
        scenario = first_present(node, SCENARIO_KEYS) or context.get("scenario")
        sdk = first_present(node, SDK_KEYS) or context.get("sdk")
        instance_id = first_present(node, INSTANCE_ID_KEYS) or context.get("instance_id")
        rate = first_present(node, PASS_RATE_KEYS)
        normalized_rate = normalize_rate(rate)

        if scenario:
            next_context["scenario"] = str(scenario)
        if sdk:
            next_context["sdk"] = str(sdk)
        if instance_id:
            next_context["instance_id"] = str(instance_id)

        explicit_instance_id = first_present(node, INSTANCE_ID_KEYS)
        explicit_scenario = first_present(node, SCENARIO_KEYS)
        explicit_sdk = first_present(node, SDK_KEYS)
        if normalized_rate is not None and (explicit_instance_id or (explicit_scenario and explicit_sdk)):
            label = (
                f"{next_context['scenario']} × {next_context['sdk']}"
                if next_context.get("scenario") and next_context.get("sdk")
                else str(explicit_instance_id or next_context.get("instance_id"))
            )
            extracted[label] = {
                "label": label,
                "scenario": next_context.get("scenario"),
                "sdk": next_context.get("sdk"),
                "instance_id": next_context.get("instance_id"),
                "pass_rate": normalized_rate,
            }

        for key, value in node.items():
            if key.lower() in SKIP_CONTAINER_KEYS:
                continue
            if isinstance(value, (dict, list)):
                visit(value, next_context)

    visit(payload, {})
    return sorted(extracted.values(), key=lambda item: item["label"])


def load_results(output_path: Path) -> list[dict[str, Any]]:
    if not output_path.exists():
        raise MsbenchEvalError(f"Report file not found: {output_path}")

    try:
        payload = json.loads(output_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise MsbenchEvalError(f"Could not parse report JSON: {output_path}\n{exc}") from exc

    results = extract_instance_results(payload)
    if not results:
        raise MsbenchEvalError(
            "Could not find per-instance pass-rate records in the report JSON."
        )
    return results


HARBOR_OUTPUT_BASENAMES = {"eval.json", "pytest.log", "reward.txt"}
# Inner per-instance archive names look like:
#   cosmos-sdk-skills.eval.x86_64.<instance>-output.zip
_INNER_OUTPUT_RE = re.compile(r"\.eval\.[^.]+\.(?P<instance>.+?)-output\.zip$")
_SAVED_RESULTS_RE = re.compile(r"Saved results to\s+(?P<path>\S+results\.zip)")
_PYTEST_FAILED_RE = re.compile(r"^FAILED\s+(?P<node>.+?)(?:\s+-\s+.*)?$", re.MULTILINE)
_PYTEST_COUNT_RES = {
    "failed": re.compile(r"(\d+)\s+failed"),
    "passed": re.compile(r"(\d+)\s+passed"),
    "skipped": re.compile(r"(\d+)\s+skipped"),
}


def _default_runs_dirs() -> list[Path]:
    """Candidate directories where msbench-cli stores per-run results.zip."""
    candidates: list[Path] = []
    local_appdata = os.getenv("LOCALAPPDATA")
    if local_appdata:
        candidates.append(Path(local_appdata) / "Microsoft" / "msbench" / "runs")
    home = Path.home()
    candidates.extend(
        [
            home / ".local" / "share" / "msbench" / "runs",
            home / ".cache" / "msbench" / "runs",
            home / ".msbench" / "runs",
            home / "AppData" / "Local" / "Microsoft" / "msbench" / "runs",
        ]
    )
    seen: set[str] = set()
    unique: list[Path] = []
    for candidate in candidates:
        key = str(candidate)
        if key not in seen:
            seen.add(key)
            unique.append(candidate)
    return unique


def extract_saved_results_paths(text: str) -> list[Path]:
    """Parse ``Saved results to <path>`` lines from resume/report output."""
    paths: list[Path] = []
    seen: set[str] = set()
    for match in _SAVED_RESULTS_RE.finditer(text):
        raw = match.group("path").strip().strip('"')
        if raw and raw not in seen:
            seen.add(raw)
            paths.append(Path(raw))
    return paths


def extract_all_run_ids(text: str) -> list[str]:
    """Collect every ``run_id=<id>`` attempt id from run submission output."""
    ids: list[str] = []
    seen: set[str] = set()
    for match in re.finditer(r"run_id=([A-Za-z0-9._:-]+)", text):
        run_id = match.group(1).strip().strip('"')
        if run_id and run_id not in seen:
            seen.add(run_id)
            ids.append(run_id)
    return ids


def find_results_zips(run_ids: list[str]) -> list[Path]:
    """Locate results.zip files for the given run ids in known runs dirs."""
    found: list[Path] = []
    seen: set[str] = set()
    for run_id in run_ids:
        for runs_dir in _default_runs_dirs():
            candidate = runs_dir / run_id / "results.zip"
            key = str(candidate)
            if candidate.is_file() and key not in seen:
                seen.add(key)
                found.append(candidate)
    return found


def _instance_from_inner_name(name: str) -> str | None:
    base = name.rsplit("/", 1)[-1]
    match = _INNER_OUTPUT_RE.search(base)
    if match:
        return match.group("instance")
    return None


def _parse_failing_tests(pytest_log: str) -> tuple[list[str], dict[str, int]]:
    """Return failing pytest node ids and a failed/passed/skipped count map."""
    failing: list[str] = []
    seen: set[str] = set()
    for match in _PYTEST_FAILED_RE.finditer(pytest_log):
        node = match.group("node").strip()
        # Drop the leading path component (e.g. "../verifier::") for readability.
        cleaned = node.split("::", 1)[-1] if "::" in node else node
        label = node
        if cleaned and cleaned not in seen:
            seen.add(cleaned)
            failing.append(label)
    counts = {"failed": 0, "passed": 0, "skipped": 0}
    # The final summary line (e.g. "12 failed, 21 passed, 12 skipped ...") carries
    # the authoritative totals. Scan bottom-up for the first line that reports any.
    for line in reversed(pytest_log.splitlines()):
        if "passed" not in line and "failed" not in line and "skipped" not in line:
            continue
        line_counts = {}
        for key, pattern in _PYTEST_COUNT_RES.items():
            match = pattern.search(line)
            if match:
                line_counts[key] = int(match.group(1))
        if line_counts:
            counts.update(line_counts)
            break
    return failing, counts


def _read_inner_output(inner_bytes: bytes) -> dict[str, bytes]:
    files: dict[str, bytes] = {}
    try:
        with zipfile.ZipFile(io.BytesIO(inner_bytes)) as inner_zip:
            for name in inner_zip.namelist():
                base = name.rsplit("/", 1)[-1]
                if base in HARBOR_OUTPUT_BASENAMES:
                    files[base] = inner_zip.read(name)
    except zipfile.BadZipFile:
        return {}
    return files


def read_instance_evals(results_zip: Path) -> dict[str, dict[str, Any]]:
    """Read per-instance harbor grading results from one attempt's results.zip.

    Returns a mapping of ``instance_id -> {resolved, failing_tests, counts}``.
    Empty when the archive contains no gradable ``eval.json`` (e.g. the 22-byte
    empty output.zip produced when the remote backend never ran the verifier).
    """
    instances: dict[str, dict[str, Any]] = {}
    try:
        with zipfile.ZipFile(results_zip) as outer_zip:
            inner_names = [n for n in outer_zip.namelist() if n.endswith("output.zip")]
            for inner_name in inner_names:
                inner_bytes = outer_zip.read(inner_name)
                if len(inner_bytes) <= 64:
                    # Empty/placeholder output.zip -> the remote backend did not grade this instance.
                    continue
                files = _read_inner_output(inner_bytes)
                if "eval.json" not in files:
                    continue
                try:
                    eval_payload = json.loads(files["eval.json"].decode("utf-8"))
                except (json.JSONDecodeError, UnicodeDecodeError):
                    continue
                failing: list[str] = []
                counts = {"failed": 0, "passed": 0, "skipped": 0}
                if "pytest.log" in files:
                    failing, counts = _parse_failing_tests(
                        files["pytest.log"].decode("utf-8", errors="replace")
                    )
                fallback_instance = _instance_from_inner_name(inner_name)
                for instance_id, verdict in eval_payload.items():
                    if isinstance(verdict, dict):
                        resolved = bool(verdict.get("resolved"))
                    else:
                        resolved = bool(verdict)
                    instances[str(instance_id)] = {
                        "resolved": resolved,
                        "failing_tests": failing,
                        "counts": counts,
                    }
                # Some eval.json use a bare {"resolved": ...} without the id key.
                if not eval_payload and fallback_instance:
                    instances[fallback_instance] = {
                        "resolved": False,
                        "failing_tests": failing,
                        "counts": counts,
                    }
    except (zipfile.BadZipFile, FileNotFoundError):
        return {}
    return instances


def _split_scenario_sdk(instance_id: str) -> tuple[str, str]:
    if "-" in instance_id:
        scenario, sdk = instance_id.rsplit("-", 1)
        return scenario, sdk
    return instance_id, "unknown"


def build_results_from_eval(
    results_zips: list[Path], benchmark: str, output_path: Path, run_id: str | None = None
) -> list[dict[str, Any]] | None:
    """Aggregate eval.json across attempts and write a results.json report.

    Returns the per-instance summary rows for threshold checking, or ``None``
    when no gradable eval.json was found (caller should fall back to the
    msbench-cli report path).
    """
    attempts: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for results_zip in results_zips:
        for instance_id, record in read_instance_evals(results_zip).items():
            attempts[instance_id].append(record)

    if not attempts:
        return None

    report_results: list[dict[str, Any]] = []
    summary_rows: list[dict[str, Any]] = []
    for instance_id in sorted(attempts):
        records = attempts[instance_id]
        total_runs = len(records)
        resolved_runs = sum(1 for record in records if record["resolved"])
        pass_rate = resolved_runs / total_runs if total_runs else 0.0
        scenario, sdk = _split_scenario_sdk(instance_id)

        failing_names: list[str] = []
        seen_names: set[str] = set()
        for record in records:
            for name in record["failing_tests"]:
                if name not in seen_names:
                    seen_names.add(name)
                    failing_names.append(name)

        report_results.append(
            {
                "instance_id": instance_id,
                "scenario": scenario,
                "sdk": sdk,
                "pass_rate": pass_rate,
                "total_runs": total_runs,
                "failed_runs": total_runs - resolved_runs,
                "failing_tests": [{"name": name, "failed": True} for name in failing_names],
            }
        )
        summary_rows.append(
            {
                "label": f"{scenario} × {sdk}",
                "scenario": scenario,
                "sdk": sdk,
                "instance_id": instance_id,
                "pass_rate": pass_rate,
            }
        )

    payload = {
        "benchmark": benchmark,
        "source": "eval.json",
        "run_id": run_id,
        "analysis_url": run_analysis_url(run_id) if run_id else None,
        "results": report_results,
    }
    output_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(f"Wrote eval.json-derived report ({len(report_results)} instances) to {output_path}")
    return sorted(summary_rows, key=lambda item: item["label"])


def print_summary(results: list[dict[str, Any]], threshold: float) -> None:
    headers = ("Instance", "Pass Rate", "Threshold", "Status")
    rows = []
    for item in results:
        status = "PASS" if item["pass_rate"] >= threshold else "FAIL"
        rows.append(
            (
                item["label"],
                f"{item['pass_rate'] * 100:.1f}%",
                f"{threshold * 100:.1f}%",
                status,
            )
        )

    widths = [len(header) for header in headers]
    for row in rows:
        widths = [max(width, len(column)) for width, column in zip(widths, row)]

    def format_row(row: tuple[str, str, str, str]) -> str:
        return " | ".join(column.ljust(width) for column, width in zip(row, widths))

    print("\nSummary")
    print(format_row(headers))
    print("-+-".join("-" * width for width in widths))
    for row in rows:
        print(format_row(row))


def main() -> int:
    args = parse_args()
    output_path = resolve_output_path(args.output)

    if args.dry_run:
        print("Dry run enabled; no commands will be executed.")
    else:
        ensure_msbench_installed(dry_run=False)

    initial = run_command(build_run_command(args), dry_run=args.dry_run, check=True)

    if args.no_wait:
        if args.dry_run:
            print(
                "Dry run: would submit with --no-wait and assert a server-assigned run_id."
            )
            return 0
        combined_initial_output = f"{initial.stdout}\n{initial.stderr}"
        run_id = extract_run_id(combined_initial_output)
        if not run_id:
            raise MsbenchEvalError(
                "Remote-backend submit preflight FAILED: msbench-cli returned no run_id, so no run "
                "was created on the remote execution backend. The calling identity could not authenticate, is not "
                "entitled to submit, or the remote backend was unreachable. See the CLI output above for "
                "the specific auth/entitlement/network error."
            )
        print(
            f"Remote-backend submit preflight OK: the remote backend assigned run_id={run_id}. A server-assigned "
            "run_id confirms the calling identity authenticated to the remote backend and is entitled to "
            "create a run. Skipping polling, report generation, and threshold checks."
        )
        print_run_link(run_id)
        return 0

    if args.dry_run:
        print(
            "> msbench-cli resume --run_id <run_id>  # if the initial run is still in progress"
        )
        print(f"> {quote_command(build_report_command('<run_id>', output_path))}")
        print(
            "> python scripts/create-skills-issue.py --results-file "
            f"{output_path}  # only if threshold failures are found"
        )
        return 0

    combined_initial_output = f"{initial.stdout}\n{initial.stderr}"
    run_id = extract_run_id(combined_initial_output)
    if not run_id:
        raise MsbenchEvalError(
            "Unable to determine run_id from msbench-cli output."
        )
    attempt_run_ids = extract_all_run_ids(combined_initial_output) or [run_id]
    # Surface the viewable run link as soon as the run_id is known, so it is
    # available even if grading/reporting fails later.
    print_run_link(run_id)

    initial_status = extract_status(combined_initial_output)
    resume_output = ""
    # `msbench-cli run` (without --no-wait) blocks until the run finishes, so the
    # captured output already contains the full run log. That log includes
    # msbench-cli's own report/analysis prose, which mis-parses harbor eval.json
    # and can surface a bogus "error" failure token (see build_results_from_eval
    # below). Treating a failure-looking status scraped from this blob as a hard
    # abort would skip the authoritative eval.json grading entirely and crash the
    # pipeline on every completed run. So we only use the status to decide whether
    # additional polling is still required; the real verdict comes from eval.json.
    if initial_status not in TERMINAL_SUCCESS and initial_status not in TERMINAL_FAILURE:
        resume_output = wait_for_completion(run_id, args)

    # Ensure results.zip archives are downloaded locally and capture the
    # "Saved results to <path>" lines emitted for every batch member.
    final_resume = run_command(build_resume_command(run_id, args), dry_run=False, check=False)
    if final_resume:
        resume_output = f"{resume_output}\n{final_resume.stdout}\n{final_resume.stderr}"

    results_zips = extract_saved_results_paths(resume_output)
    if not results_zips:
        results_zips = find_results_zips(attempt_run_ids)

    # Primary path: read authoritative eval.json (+ pytest failures) straight
    # from each downloaded output.zip. The msbench-cli report mis-parses harbor
    # eval.json (EvalJsonParser.parse_log) and reports "error", so we bypass it.
    results = build_results_from_eval(results_zips, args.benchmark, output_path, run_id)

    if not results:
        # Fallback for non-harbor benchmarks: use the msbench-cli merged report.
        print(
            "No gradable eval.json found in downloaded results; "
            "falling back to msbench-cli report.",
            file=sys.stderr,
        )
        run_command(build_report_command(run_id, output_path), dry_run=False, check=True)
        results = load_results(output_path)

    print_summary(results, args.threshold)
    # Repeat the link after the summary so it is the last, most-visible line.
    print_run_link(run_id)

    failing = [item for item in results if item["pass_rate"] < args.threshold]
    if failing:
        print(
            f"\n{len(failing)} instance(s) fell below the {args.threshold * 100:.1f}% threshold.",
            file=sys.stderr,
        )
        return 1

    print("\nAll instances met the configured threshold.")
    return 0


if __name__ == "__main__":
    try:
        raise SystemExit(main())
    except MsbenchEvalError as exc:
        print(f"ERROR: {exc}", file=sys.stderr)
        raise SystemExit(2)
